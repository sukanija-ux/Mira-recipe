function phaseForDay(day, length = 28) {
  const d = ((day - 1) % length) + 1;
  const pct = d / length;
  if (pct <= 5 / 28) return PHASES[0];
  if (pct <= 13 / 28) return PHASES[1];
  if (pct <= 17 / 28) return PHASES[2];
  return PHASES[3];
}

